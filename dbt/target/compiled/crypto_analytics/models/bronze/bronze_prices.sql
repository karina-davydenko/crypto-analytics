-- bronze_prices: бронзовый слой для цен криптовалют.
--
-- Бронзовый слой — это "зеркало" сырых данных.
-- Мы не трансформируем данные, только добавляем технические поля.
-- Цель: изолировать остальные слои от прямого обращения к raw таблицам.
-- Если структура raw_prices изменится — правим только этот файл.
--
-- Материализация: VIEW (не таблица).
-- VIEW не занимает место на диске — это просто сохранённый SQL запрос.
-- При каждом обращении к bronze_prices данные читаются из raw_prices в реальном времени.
-- Это нормально для бронзового слоя, где данных немного и трансформаций нет.

select
    -- Первичный ключ из сырой таблицы
    id,

    -- Идентификатор монеты (всегда в нижнем регистре, приходит с CoinGecko)
    coin_id,

    -- Полное название монеты
    coin_name,

    -- Тикер монеты (приводим к верхнему регистру — на случай если придут данные в другом регистре)
    upper(symbol) as symbol,

    -- Цена в долларах
    price_usd,

    -- Рыночная капитализация
    market_cap,

    -- Объём торгов за 24 часа
    volume_24h,

    -- Изменение цены за 24 часа в процентах
    price_change_24h,

    -- Время получения данных с API — оставляем как есть, это важная метаинформация
    fetched_at,

    -- Технические поля добавляемые dbt:
    -- _loaded_at — когда dbt последний раз запускал этот запрос.
    -- Позволяет понять свежесть данных в bronze слое.
    current_timestamp as _loaded_at

-- source() — функция dbt которая ссылается на таблицу объявленную в sources.yml.
-- Лучше чем писать `public.raw_prices` напрямую:
-- dbt понимает зависимости и строит граф трансформаций.
from "crypto_analytics"."public"."raw_prices"